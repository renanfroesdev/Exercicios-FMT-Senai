// Exercicio 5 - Utilize uma estrutura condicional (if,else) 
// para verificar se um número é positivo, negativo ou zero. 
// Exiba os valores no console.log()

let num = prompt('Digite um número para verificar: ')
if (num == 0) {
    console.log('O número digitado é Zero')
}else if (num > 0){
    console.log('O número digitado é Positivo')
}else {
    console.log('O número digitado é Negativo')
}